package model;

/**
 *
 * @author ADMIN
 */
public class Student {
    private int ID;
    private String ho;
    private String ten;
    private int phone;
    private String address;

    public Student(int ID, String ho, String ten, int phone, String address) {
        this.ID = ID;
        this.ho = ho;
        this.ten = ten;
        this.phone = phone;
        this.address = address;
    }

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public String getHo() {
        return ho;
    }

    public void setHo(String ho) {
        this.ho = ho;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public int getPhone() {
        return phone;
    }

    public void setPhone(int phone) {
        this.phone = phone;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    @Override
    public String toString() {
        return "Student{" + "ID=" + ID + ", ho=" + ho + ", ten=" + ten + ", phone=" + phone + ", address=" + address + '}';
    }
    
}
