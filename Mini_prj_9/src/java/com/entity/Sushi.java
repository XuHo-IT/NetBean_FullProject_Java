package com.entity;

/**
 *
 * @author ADMIN
 */
public class Sushi {
    private int id;
    private String name;
    private float price;
    private String content;

    public Sushi(int id, String name, float price, String content) {
        this.id = id;
        this.name = name;
        this.price = price;
        this.content = content;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public float getPrice() {
        return price;
    }

    public void setPrice(float price) {
        this.price = price;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

   
    public Sushi() {
    }
    
}
